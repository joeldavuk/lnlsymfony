<?php

namespace lnl\lnlBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Meta
 */
class Meta
{
    /**
     * @var integer
     */
    private $id;

    /**
     * @var string
     */
    private $itemType;

    /**
     * @var string
     */
    private $itemKey;

    /**
     * @var string
     */
    private $itemValue;

    /**
     * @var \lnl\lnlBundle\Entity\Items
     */
    private $items;


    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set itemType
     *
     * @param string $itemType
     * @return Meta
     */
    public function setItemType($itemType)
    {
        $this->itemType = $itemType;

        return $this;
    }

    /**
     * Get itemType
     *
     * @return string 
     */
    public function getItemType()
    {
        return $this->itemType;
    }

    /**
     * Set itemKey
     *
     * @param string $itemKey
     * @return Meta
     */
    public function setItemKey($itemKey)
    {
        $this->itemKey = $itemKey;

        return $this;
    }

    /**
     * Get itemKey
     *
     * @return string 
     */
    public function getItemKey()
    {
        return $this->itemKey;
    }

    /**
     * Set itemValue
     *
     * @param string $itemValue
     * @return Meta
     */
    public function setItemValue($itemValue)
    {
        $this->itemValue = $itemValue;

        return $this;
    }

    /**
     * Get itemValue
     *
     * @return string 
     */
    public function getItemValue()
    {
        return $this->itemValue;
    }

    /**
     * Set items
     *
     * @param \lnl\lnlBundle\Entity\Items $items
     * @return Meta
     */
    public function setItems(\lnl\lnlBundle\Entity\Items $items = null)
    {
        $this->items = $items;

        return $this;
    }

    /**
     * Get items
     *
     * @return \lnl\lnlBundle\Entity\Items 
     */
    public function getItems()
    {
        return $this->items;
    }
}
